@app.route('/api/chat', methods=['POST'])
def chat():
    """Handle chatbot messages"""
    try:
        data = request.json
        question = data.get('question', '').strip()
        session_id = data.get('session_id', str(uuid.uuid4()))
        
        if not question:
            return jsonify({
                'error': 'No question provided',
                'session_id': session_id
            }), 400
        
        # Process question
        response = chatbot_instance.process_question(question, session_id)
        
        return jsonify({
            'success': True,
            'response': response['response'],
            'category': response['category'],
            'suggested_questions': response['suggested_questions'],
            'session_id': session_id,
            'timestamp': response['timestamp']
        })
        
    except Exception as e:
        return jsonify({
            'error': str(e),
            'success': False
        }), 500

@app.route('/api/contact', methods=['POST'])
def contact():
    """Handle contact form submissions (for About page)"""
    try:
        data = request.json
        name = data.get('name', '').strip()
        email = data.get('email', '').strip()
        subject = data.get('subject', '').strip()
        message = data.get('message', '').strip()
        
        # Basic validation
        if not all([name, email, subject, message]):
            return jsonify({
                'error': 'All fields are required',
                'success': False
            }), 400
        
        if '@' not in email or '.' not in email:
            return jsonify({
                'error': 'Invalid email address',
                'success': False
            }), 400
        
        # Save to database
        success = chatbot_instance.save_contact_form(name, email, subject, message)
        
        if success:
            # You could also send email here if needed
            return jsonify({
                'success': True,
                'message': 'Thank you for your message! We will get back to you soon.'
            })
        else:
            return jsonify({
                'error': 'Failed to save message',
                'success': False
            }), 500
        
    except Exception as e:
        return jsonify({
            'error': str(e),
            'success': False
        }), 500

@app.route('/api/chat/history', methods=['GET'])
def chat_history():
    """Get chat history for a session"""
    try:
        session_id = request.args.get('session_id')
        if not session_id:
            return jsonify({
                'error': 'Session ID required',
                'success': False
            }), 400
        
        history = chatbot_instance.get_conversation_history(session_id)
        
        return jsonify({
            'success': True,
            'history': history,
            'count': len(history)
        })
        
    except Exception as e:
        return jsonify({
            'error': str(e),
            'success': False
        }), 500

